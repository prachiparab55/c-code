#include <iostream>
using namespace std;

int main() 
{
    int y = 5;

    cout << "Pre-increment (++y): " << ++y << endl;   
    cout << "Post-increment (y++): " << y++ << endl; 
    cout << "Pre-decrement (--y): " << --y << endl;  
    cout << "Post-decrement (y--): " << y-- << endl;  

    return 0;
}
